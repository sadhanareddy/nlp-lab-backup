<body>
Given the root and suffix information, a word can be generated. For example, <br><br>

<pre>
<b>Language   input:analysis			      output:word</b>
Hindi      rt=लड़का, cat=n, gen=m, num=sg, case=obl      लड़के
           rt=लड़का, cat=n, gen=m, num=pl, case=dir      लड़के

English    rt=boy, cat=n, num=pl			boys
           rt=play, cat=v, num=sg, per=3, tense=pr      plays
</pre><br>

- Morphological analysis and generation: Inverse processes.<br>
- Analysis may involve non-determinism, since more than one analysis is possible.<br>
- Generation is a deterministic process. In case a language allows spelling variation, to that extent, generation also 
  involves non-determinism.

